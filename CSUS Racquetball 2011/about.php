	<p>&nbsp;</p>
	<p>We are a club sport at California State University Sacramento. The racquetball team won two national titles in 1986 and 1987 led by #1 guy Michael Bronfeld and #1 girl Robin Levine. The team then disbanded after the late 1980's and was refounded by our coach, Mark Salinas, in 2000. Our team has improved in rank and gained members every year since 2000. We hope to keep growing and get more people interested in Racquetball.</p>
	<p>&nbsp;</p>
	<p>The team is co-ed and accepts <em>ALL</em> levels of players from first time beginners to the top &quot;open&quot; level players. The team is a great place to meet other young racquetball players for competitive and fun matches alike. The team is involved in the WCRC (Western Collegiate Racquetball Conference), which is a league that holds three tournaments yearly.&nbsp; The league is composed of surrounding colleges including UC Berkeley, UC Davis, Standford, UC Santa Cruz, CSU San Jose, Goldenwest College, CSU Fullerton, UC San Diego, Butte Junior College, Solano Community College, etc. Nationals is the annual tournament in which we will compete against Racquetball teams from colleges all across the nation. In spring of 2004 the racquetball club closed its season by finishing fourth among 48 teams at the National Intercollegiate Racquetball Championships. The three squads that finished ahead of CSUS &ndash; Alabama, Oregon State and BYU &ndash; are all scholarship schools and the Hornets finished in front of such schools as Arizona State and UC Berkeley.</p>
